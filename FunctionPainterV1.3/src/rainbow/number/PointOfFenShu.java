package rainbow.number;

public class PointOfFenShu {

	private FenShu x;
	private FenShu y;

	public PointOfFenShu(FenShu x, FenShu y) {
		this.x = x;
		this.y = y;
	}

	public FenShu getX() {
		return x;
	}

	public void setX(FenShu x) {
		this.x = x;
	}

	public FenShu getY() {
		return y;
	}

	public void setY(FenShu y) {
		this.y = y;
	}

}
